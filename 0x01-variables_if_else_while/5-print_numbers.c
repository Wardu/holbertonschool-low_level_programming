#include <stdio.h>

/**
 * main - Entry a number to the variable c and print number.
 *
 * Description: prints all single digit numbers of base 10 starting from 0.
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int c;

	for (c = 0 ; c < 10 ; c++)
	{
		printf("%i", c);
	}
	printf("\n");

return (0);
}
